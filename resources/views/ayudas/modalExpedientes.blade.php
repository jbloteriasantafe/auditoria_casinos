Esta ventana permite la creación de un expediente y de su correspondiente resolución como así también de sus disposiciones.
Además, se habilitan dos pestañas para crear NOTAS.
<br><br>

<h5>CONFIGURACIÓN</h5>
En esta pestaña se ingresan los datos que identifican al expediente.
El NÚMERO DE EXPEDIENTE, el(los) CASINO(S) correspondiente al expediente y la CANTIDAD DE CUERPOS
son los campos mínimos y obligatorios para crear el expediente.
<br><br>

<h5>NOTAS</h5>
Para crear notas enlazadas al expediente, esta pestaña se habilitará al seleccionar uno o más casinos
en la pestaña de configuración.<br>
Presione AGREGAR NOTA Y MOVIMIENTO NUEVO para crear una NOTA. Todos los campos de la nota son obligatorios.
<br><br>

<h5>NOTAS & MOVIMIENTOS</h5>
Los movimientos ya creados en el sistema estarán disponibles en esta pestaña para poder enlazarlos a una nueva NOTA.
Todos los campos son obligatorios.
<br>
Para habilitar esta sección debe seleccionar SOLO UN CASINO en la pestaña de configuración.
